package game;

import java.util.ArrayList;
import java.util.List;
import java.util.Random;

import edu.monash.fit2099.engine.Action;
import edu.monash.fit2099.engine.DoNothingAction;
import edu.monash.fit2099.engine.Exit;
import edu.monash.fit2099.engine.GameMap;
import edu.monash.fit2099.engine.Location;
/**
 * Behavior for AI to wonder arounds anywhere but 
 * do nothing if there's isn't a way to move AI.
 */
public class WanderBehaviour implements Behaviour {
	
	private Random random = new Random();

	/**
	 * Returns a MoveAction to wander to a random location, if possible.  
	 * If no movement is possible, returns null.
	 * 
	 * @param actor the Actor enacting the behaviour
	 * @param map the map that actor is currently on
	 * @return an Action, or null if no MoveAction is possible
	 */
	@Override
	public Action getAction(GameCharacter actor, GameMap map) {
		ArrayList<Action> actions = new ArrayList<Action>();
		GameCharacter character = (GameCharacter) actor;
		List<Exit> movement = character.returnExits(map);
		for (Exit exit : movement) {
            Location destination = exit.getDestination();
            if (destination.canActorEnter(actor)) {
            	actions.add(exit.getDestination().getMoveAction(actor, "around", exit.getHotKey()));
            }
        }
		
		if (!actions.isEmpty()) {
			return actions.get(random.nextInt(actions.size()));
		}
		else {
			return new DoNothingAction();
		}

	}

}
